    Henry Frank Phillips (1890 — 1958) foi um empresario americano de Portland, Oregon. Ele dá nome aos parafusos e chaves Phillips, também chamado de cruzeta.[1]

A importância do design do parafuso cruzeta esta na sua propriedade de autocentrado, úteis em linhas de produção automatizadas que utilizam chaves de parafusos automáticas. A grande contribuição de Phillips estava em levar a adoção do conceito por fabricantes de parafusos e empresas de automóveis.
    </p>
    <p>
    Engenheiro, Phillips era um conhecido de John P. Thompson,[2] que vendeu seu projeto de auto-centramento a Phillips, em 1935, depois de não conseguir fabricantes interessados. Phillips formou a Companhia de Parafuso Phillips, em 1934, e depois de refinar o design próprio (EUA Patente # 2046343, Patentes dos EUA # 2046837 a 2046840) para a Companhia de parafuso americana de Providence, Rhode Island, conseguiu que o projeto fosse rapidamente adotado pela indústria.[3] Um dos primeiros clientes, em 1937, foi a General Motors para as linhas de montagem do Cadillac. Em 1940, 85% dos fabricantes americanos de parafuso tinha uma licença para o projeto.

Devido a problemas de saúde, Phillips se aposentou em 1945. Ele morreu em 1958.
